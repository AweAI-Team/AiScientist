from abc import ABC, abstractmethod
from dataclasses import dataclass
from typing import Any

from openai.types.responses import FunctionToolParam
from pydantic import BaseModel

from nanoeval.solvers.computer_tasks.code_execution_interface import ComputerInterface


class Tool(BaseModel, ABC):
    """
    Abstract base class for BasicAgent tools.
    Custom tools used by BasicAgent should implement this interface.

    - `name` is used to identify the tool
    - `execute` is called to run the tool, typically to run commands on the
      computer
    - `get_oai_tool_call` returns the OpenAI function tool schema defining this tool's usage
    - `execute_with_constraints` (optional) is called when constraints need to be applied
      (e.g., filtering blacklisted URLs from search results)
    """

    @abstractmethod
    def name(self) -> str:
        """
        Returns the unique name of this tool.
        """
        ...

    @abstractmethod
    async def execute(self, computer: ComputerInterface, *args: Any, **kwargs: Any) -> str:
        """
        Executes the tool with the given arguments and returns output.
        """
        ...

    @abstractmethod
    def get_oai_tool_call(self) -> FunctionToolParam:
        """
        Returns the OpenAI Responses function tool schema for this tool.
        """
        ...

    async def execute_with_constraints(
        self,
        computer: ComputerInterface,
        constraints: dict | None = None,
        *args: Any,
        **kwargs: Any
    ) -> str:
        """
        Executes the tool with constraints applied (e.g., blacklist filtering).
        
        By default, this falls back to the regular execute method.
        Override this method in subclasses that need constraint support.
        
        Args:
            computer: ComputerInterface for executing commands
            constraints: Optional dict of constraints to apply, e.g.:
                - blocked_search_patterns: dict[str, list[str]] for filtering URLs
            *args, **kwargs: Additional arguments passed to execute
            
        Returns:
            Tool output as a string
        """
        return await self.execute(computer, *args, **kwargs)

    def supports_constraints(self) -> bool:
        """
        Returns True if this tool has custom constraint handling.
        
        Override this method and return True in subclasses that implement
        custom execute_with_constraints logic.
        """
        return False


@dataclass
class ToolCall:
    name: str
    """The name of the function to run."""

    arguments: dict[Any, Any]
    """A JSON string of the arguments to pass to the function."""

    call_id: str
    """The unique ID of the function tool call generated by the model."""
