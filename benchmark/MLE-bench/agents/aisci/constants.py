def is_file_as_bus_enabled() -> bool:
    """Whether implement/exp subagents share state via ``impl_log.md`` / ``exp_log.md``.

    Default **True** (including when ``FILE_AS_BUS`` is unset). Only explicit
    false-like values disable: ``0``, ``false``, ``no``, ``off`` (case-insensitive).
    """
    raw = os.environ.get("FILE_AS_BUS")
    if raw is None:
        return True
    return raw.strip().lower() not in ("0", "false", "no", "off")


